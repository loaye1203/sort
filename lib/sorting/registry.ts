export type RegistryEntry = {
  meta: AlgorithmMeta;
  load: ((meta: AlgorithmMeta) => Promise<SortAlgorithm>) | null;
  catalogCode?: string;
};

async function resolveImplementation(meta: AlgorithmMeta, implementations: Record<string, AlgorithmImplementation>) {
  const implementation = implementations[meta.id];
  if (!implementation) throw new Error(`Missing algorithm implementation: ${meta.id}`);
  return { ...implementation, meta };
}

async function loadFullAlgorithm(meta: AlgorithmMeta) { return resolveImplementation(meta, (await import("./algorithms/full")).algorithms); }
async function loadSimplifiedAlgorithm(meta: AlgorithmMeta) { return resolveImplementation(meta, (await import("./algorithms/simplified")).algorithms); }
async function loadSimulatedAlgorithm(meta: AlgorithmMeta) { return resolveImplementation(meta, (await import("./algorithms/simulated")).algorithms); }

