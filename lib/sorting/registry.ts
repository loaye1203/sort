export const algorithmRegistryById = new Map(
  algorithmRegistry.map((entry) => [entry.meta.id, entry]),
);

export function findRegistryEntry(id: string) { return algorithmRegistryById.get(id) ?? algorithmRegistry[0]; }

export function getSafetyLimit(meta: AlgorithmMeta): SafetyLimit {
  const base = riskyAlgorithmSafetyLimits[meta.id] ?? safetyLimitsByMode[meta.runMode];

  return {
    maxSteps: base.maxSteps,
    maxRuntimeMs: base.maxRuntimeMs,
    maxArraySize: Math.min(base.maxArraySize, meta.recommendedSize.max),
  };
}

export function searchAlgorithms(query: string) {
  const needle = query.trim().toLowerCase();

  if (!needle) {
    return algorithmRegistry;
  }

  return algorithmRegistry.filter(({ meta }) => {
    const haystack = [
      meta.name,
      meta.englishName,
      meta.category,
      meta.description,
      meta.jokeDescription ?? "",
      ...meta.aliases,
    ]
      .join(" ")
      .toLowerCase();

    return haystack.includes(needle);
  });
}
